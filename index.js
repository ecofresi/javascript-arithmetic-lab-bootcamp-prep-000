function add(a,b) {
  return a + b;
}

function subtract(a,b) {
  return a - b;
}

function mmultiply(a,b) {
  return a * b;
}

function division(a,b) {
  return a / b;
}

function inc(n) {
  return n+=1;
}

function dec(n) {
  return n-=1;
}

function makeInt(n) {
  return parseInt(n, 10);
}

function perserveDecimal(n) {
  return parseFloat(n);
}



